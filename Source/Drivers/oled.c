// 函数声明
static void OLED_I2C_Start(void);
static void OLED_I2C_Stop(void);
static void OLED_I2C_SendByte(uint8_t byte);
static uint8_t OLED_I2C_ReadByte(uint8_t ack);
static void OLED_WriteCmd(uint8_t cmd);
static void OLED_WriteData(uint8_t data);

/**
 * @brief I2C开始信号
 */
static void OLED_I2C_Start(void)
{
    OLED_SDA_SET();
    OLED_SCL_SET();
    Delay_us(4);
    OLED_SDA_CLR();
    Delay_us(4);
    OLED_SCL_CLR();
    Delay_us(4);
}

/**
 * @brief I2C停止信号
 */
static void OLED_I2C_Stop(void)
{
    OLED_SCL_CLR();
    Delay_us(4);
    OLED_SDA_CLR();
    Delay_us(4);
    OLED_SCL_SET();
    Delay_us(4);
    OLED_SDA_SET();
    Delay_us(4);
}

/**
 * @brief I2C发送一个字节
 */
static void OLED_I2C_SendByte(uint8_t byte)
{
    uint8_t i;
    for(i = 0; i < 8; i++)
    {
        if(byte & 0x80)
            OLED_SDA_SET();
        else
            OLED_SDA_CLR();
        OLED_SCL_SET();
        Delay_us(2);
        OLED_SCL_CLR();
        Delay_us(2);
        byte <<= 1;
    }
    // 等待ACK
    OLED_SDA_SET();
    OLED_SCL_SET();
    Delay_us(2);
    OLED_SCL_CLR();
    Delay_us(2);
}

/**
 * @brief I2C读取一个字节
 */
static uint8_t OLED_I2C_ReadByte(uint8_t ack)
{
    uint8_t i, byte = 0;
    OLED_SDA_SET();
    for(i = 0; i < 8; i++)
    {
        OLED_SCL_SET();
        Delay_us(2);
        byte <<= 1;
        if(OLED_SDA_READ())
            byte |= 0x01;
        OLED_SCL_CLR();
        Delay_us(2);
    }
    if(ack)
        OLED_SDA_CLR();
    OLED_SCL_SET();
    Delay_us(2);
    OLED_SCL_CLR();
    Delay_us(2);
    OLED_SDA_SET();
    return byte;
}

/**
 * @brief OLED写入命令
 */
static void OLED_WriteCmd(uint8_t cmd)
{
    OLED_I2C_Start();
    OLED_I2C_SendByte(0x78);  // OLED地址0x3C左移一位=0x78
    OLED_I2C_SendByte(0x00);  // 写入命令
    OLED_I2C_SendByte(cmd);
    OLED_I2C_Stop();
}

/**
 * @brief OLED写入数据
 */
static void OLED_WriteData(uint8_t data)
{
    OLED_I2C_Start();
    OLED_I2C_SendByte(0x78);  // OLED地址
    OLED_I2C_SendByte(0x40);  // 写入数据
    OLED_I2C_SendByte(data);
    OLED_I2C_Stop();
}

/**
 * @brief OLED初始化
 */
void OLED_Init(void)
{
    GPIO_InitTypeDef GPIO_InitStructure;
    uint16_t i;
    
    // 打开GPIOB时钟
    RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOB, ENABLE);
    
    // 配置SCL和SDA为开漏输出
    GPIO_InitStructure.GPIO_Pin = OLED_SCL_PIN;
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_OD;
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_Init(OLED_SCL_PORT, &GPIO_InitStructure);
    
    GPIO_InitStructure.GPIO_Pin = OLED_SDA_PIN;
    GPIO_Init(OLED_SDA_PORT, &GPIO_InitStructure);
    
    // 初始化为高电平
    OLED_SCL_SET();
    OLED_SDA_SET();
    
    // OLED初始化序列
    OLED_WriteCmd(0xAE);   // --turn off oled panel
    OLED_WriteCmd(0x02);   // --low column address
    OLED_WriteCmd(0x10);   // --high column address
    OLED_WriteCmd(0x40);   // --start line address
    OLED_WriteCmd(0x81);   // --contrast data register
    OLED_WriteCmd(0x66);   // --SEG Output Current Brightness
    OLED_WriteCmd(0xA1);   // --Set SEG/Column Mapping
    OLED_WriteCmd(0xC8);   // --Set COM/Row Scan Direction
    OLED_WriteCmd(0xA6);   // --normal / reverse
    OLED_WriteCmd(0xA8);   // --multiplex ration mode:63
    OLED_WriteCmd(0x3F);
    OLED_WriteCmd(0xD3);   // --set display offset
    OLED_WriteCmd(0x00);
    OLED_WriteCmd(0xD5);   // --set osc division
    OLED_WriteCmd(0x80);
    OLED_WriteCmd(0xD9);   // --set pre-charge period
    OLED_WriteCmd(0x11);
    OLED_WriteCmd(0xDA);   // --set com pin configuartion
    OLED_WriteCmd(0x12);
    OLED_WriteCmd(0xDB);   // --set vcomh
    OLED_WriteCmd(0x20);
    OLED_WriteCmd(0x8D);   // --set charge pump enable
    OLED_WriteCmd(0x14);
    OLED_WriteCmd(0xAF);   // --turn on oled panel
    
    OLED_Clear();
}

/**
 * @brief 清屏
 */
void OLED_Clear(void)
{
    uint8_t i, j;
    for(i = 0; i < 8; i++)
    {
        OLED_WriteCmd(0xB0 + i);  // 页地址
        OLED_WriteCmd(0x00);      // 列地址（低4位）
        OLED_WriteCmd(0x10);      // 列地址（高4位）
        for(j = 0; j < 128; j++)
            OLED_WriteData(0x00);
    }
    memset(OLED_GRAM, 0, sizeof(OLED_GRAM));
}

/**
 * @brief 显示单个字符
 */
void OLED_ShowChar(uint8_t x, uint8_t y, char chr)
{
    uint8_t i = 0;
    uint8_t c = chr - ' ';  // 获取字符编号
    if(x > 127 || y > 7) return;
    
    OLED_WriteCmd(0xB0 + y);
    OLED_WriteCmd(0x00 | (x & 0x0F));
    OLED_WriteCmd(0x10 | ((x >> 4) & 0x0F));
    
    for(i = 0; i < 5; i++)
        OLED_WriteData(ASCII_Tab[c][i]);
    OLED_WriteData(0x00);
}

/**
 * @brief 显示字符串
 */
void OLED_ShowString(uint8_t x, uint8_t y, char *str)
{
    uint8_t i = 0;
    while(str[i] != '\0')
    {
        OLED_ShowChar(x + (i * 6), y, str[i]);
        i++;
    }
}

/**
 * @brief 显示数字
 */
void OLED_ShowNum(uint8_t x, uint8_t y, uint32_t num, uint8_t len)
{
    uint8_t i = 0;
    uint8_t temp = 0;
    uint8_t enshow = 0;
    for(i = (len - 1); i < 255; i--)
    {
        temp = (num / pow(10, i)) % 10;
        if(enshow == 0 && i != 0)
        {
            if(temp == 0)
                continue;
            enshow = 1;
        }
        OLED_ShowChar(x + (len - i - 1) * 6, y, '0' + temp);
    }
}

/**
 * @brief 绘制图片
 */
void OLED_DrawBMP(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1, uint8_t *BMP)
{
    uint32_t i = 0;
    uint8_t x = 0, y = 0;
    if(y1 % 8 != 0)
        y1 = y1 / 8 + 1;
    else
        y1 = y1 / 8;
    for(y = y0; y < y1; y++)
    {
        OLED_WriteCmd(0xB0 + y);
        OLED_WriteCmd(0x00);
        OLED_WriteCmd(0x10);
        for(x = x0; x < x1; x++)
        {
            OLED_WriteData(BMP[i++]);
        }
    }
}
